package com.sec02;
public class IncDecOperator {
	public static void main(String[] args) {

		int op = 10; 

		System.out.println(op++); 
		System.out.println(op); 

		System.out.println(++op); 
		System.out.println(op); 

		System.out.println(op--); 
		System.out.println(op); 

		System.out.println(--op); 
		System.out.println(op);

	}
}




