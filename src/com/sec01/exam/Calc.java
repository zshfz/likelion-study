package com.sec01.exam;

public class Calc {
	public static int getAdd(int a, int b) {
		return a + b;
	}
	
	public static int getSub(int a, int b) {
		return a - b;
	}
	
	public static int getMul(int a, int b) {
		return a * b;
	}
	
	public static double getDiv(double a, double b) {
		return a / b;
	}
}
